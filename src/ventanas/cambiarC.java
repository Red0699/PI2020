    private void BActualizarCActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BActualizarCActionPerformed
        // TODO add your handling code here:
        String passAC, passNC, passCC;
        passAC = txtAC.getText().trim();
        passNC = txtNC.getText().trim();
        passCC = txtCC.getText().trim();
        user = inicio_sesion.user;
        repaint();

        int aux = 0;

        if (passAC.equals("")) {
            txtAC.setBorderColor(Color.red);
            aux++;
        }
        if (passNC.equals("")) {
            txtNC.setBorderColor(Color.red);
            txtNC.setColorIcon(Color.red);
            aux++;
        }
        if (passCC.equals("")) {
            txtCC.setBorderColor(Color.red);
            txtCC.setColorIcon(Color.red);
            aux++;
        }
        
        if(!passNC.equals(passCC)){
            txtCC.setBorderColor(Color.red);
            txtCC.setColorIcon(Color.red);
            txtNC.setBorderColor(Color.red);
            txtNC.setColorIcon(Color.red);
            aux++;
        }

        try {
            Connection cn = Conexion.conectar();
            String sql = "SELECT contrase単a FROM usuario WHERE arias= '" + user + "'";
            PreparedStatement pst = cn.prepareStatement(sql);
            ResultSet rs = pst.executeQuery();
            while (rs.next()) {
                val = rs.getString("contrase単a");
            }
            cn.close();
        } catch (SQLException e) {

            System.err.println("Error al consultar informacion" + e);
            JOptionPane.showMessageDialog(null, "Error al consultar informaci坦n");
        }
        
        if(!val.equals(passAC)){
            txtAC.setBorderColor(Color.red);
            aux++;
        }

        if (aux == 0) {

            try {
                Connection cn2 = Conexion.conectar();
                String sql2 = "UPDATE usuario SET contrase単a=? WHERE arias = '" + user + "'";
                PreparedStatement pst2 = cn2.prepareStatement(sql2);
                
                pst2.setString(1,passNC);
                
                pst2.executeUpdate();
                cn2.close();
                
                txtAC.setBackground(Color.green);
                txtNC.setBackground(Color.green);
                txtCC.setBackground(Color.green);
                
                JOptionPane.showMessageDialog(null, "Se ha actualizado la contrase単a correctamente");
                this.dispose();

            } catch (SQLException e) {
                System.err.println("Error" + e);
                JOptionPane.showMessageDialog(null, "Error al actualizar los datos");
            }

        } else {
            JOptionPane.showMessageDialog(null, "Datos incorrectos");
            aux=0;
        }
    }//GEN-LAST:event_BActualizarCActionPerformed

